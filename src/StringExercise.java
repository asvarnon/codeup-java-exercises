public class StringExercise {

    public static void main(String[] args) {
        String edu = "We dont need no education. \n We dont need not thought control.";
        System.out.println("edu = " + edu);

        String thoughtControl = "We dont need not thought control.";
        System.out.println("thoughtControl = " + thoughtControl);

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println("check = " + check);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println("windows = " + windows);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println("backslash = " + backslash);
    }

}
